export function t(
  key: keyof typeof T,
  lang: Lang,
  vars?: Record<string, string | number>,
): string {
  let str: string = T[key]?.[lang] ?? T[key]?.['vi'] ?? String(key);
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    });
  }
  return str;
}
